/**
 * 🧓 matryoshka babushka — Пасхалка
 *
 * Выводит ASCII-арт бабушки с матрёшкой и мудрую пословицу.
 * Каждый запуск даёт +10 XP к уровню агента.
 */

import { addMatryoshkaXp, resolveLevel } from "../auto-reply/matryoshka-level.js";

// ── ASCII арт ─────────────────────────────────────────────────────────────────

const BABUSHKA_ART = `
     ,   ,
    /|   |\\
   / |   | \\
  |  |   |  |
  |  \\ _ /  |
   \\  (o o)  /
    | (___) |
    |  ~~~  |       🪆
    |       |      /|\\
    \\_______/     / | \\
     |     |     |  |  |
     |     |     | (_) |
     |_____|      \\___/
   Бабушка в деле
`;

// ── Пословицы ─────────────────────────────────────────────────────────────────

const PROVERBS = [
  "Семь раз отмери — один раз задеплой.",
  "Не баг, а фича — родина не забудет.",
  "Каков агент, таков и ответ.",
  "Лучше медленно, но верно — чем быстро в продакшен с багом.",
  "Обещанного токена три года ждут.",
  "Куй железо пока LLM горячий.",
  "На grep надейся, а сам не плошай.",
  "Что написано кодом — топором не вырубишь (пока не сделаешь git reset --hard).",
  "Дурная голова ногам покоя не даёт, а плохой промпт — агенту.",
  "Терпение и труд — всё перетрут. А потом ещё раз перетрут — context window кончился.",
  "В чужой кодобазе не ищут баги. (Но всё равно находят.)",
  "Без тестов — и жизни нет. А жизни нет — и без тестов.",
  "Хорошего агента и кринж не сломит.",
  "Слово не воробей — в промпт вылетит, не поймаешь.",
  "Знал бы где упадёт — постелил бы try/catch.",
  "Что матрёшка — то и AIent. Открываешь — а внутри ещё один.",
  "CI зелёный — значит можно в отпуск.",
  "Русский GitHub аккаунт долго запрягает, да быстро едет.",
  "Не откладывай на завтра то, что можно задеплоить сегодня.",
  "Бабушка два раза не показывает. Агент — тоже.",
];

// ── Команда ───────────────────────────────────────────────────────────────────

export function babushkaCommand(): void {
  const proverb = PROVERBS[Math.floor(Math.random() * PROVERBS.length)] ?? PROVERBS[0]!;

  // Начисляем XP
  const newLevel = addMatryoshkaXp(10);
  const xpLine = `\n+10 XP начислено! Текущий уровень: ${newLevel.emoji} ${newLevel.name}`;

  process.stdout.write(`${BABUSHKA_ART}\n`);
  process.stdout.write(`  💬 «${proverb}»\n`);
  process.stdout.write(`${xpLine}\n\n`);
}
