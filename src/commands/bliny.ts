/**
 * 🥞 matryoshka блины — Рецепт блинов от бабушки
 *
 * Настоящий разработчик должен уметь не только деплоить,
 * но и поесть. Матрёшка заботится о тебе.
 *
 * +5 XP за то, что сделал перерыв.
 */

import { addMatryoshkaXp } from "../auto-reply/matryoshka-level.js";

// ── ASCII арт ─────────────────────────────────────────────────────────────────

const BLINY_ART = `
        _____
      /       \\
    /   🥞🥞🥞  \\
   |  блины!   |
    \\  горячие /
      \\_______/
         | |
         | |
      ~~~   ~~~
`;

// ── Рецепт ───────────────────────────────────────────────────────────────────

const RECIPE = `
  📜 РЕЦЕПТ БЛИНОВ ОТ БАБУШКИ-МАТРЁШКИ
  ─────────────────────────────────────

  Ингредиенты (на ~15 блинов):
    🥛  500 мл молока
    🥚  2 яйца
    🌾  200 г муки
    🧈  2 ст.л. сливочного масла (растопить)
    🍬  1 ст.л. сахара
    🧂  щепотка соли
    🫧  0.5 ч.л. соды (гасить уксусом)

  Приготовление:
    1. Смешай яйца с сахаром и солью.
    2. Влей молоко, добавь муку — мешай до однородности.
    3. Добавь масло и соду с уксусом.
    4. Дай тесту отдохнуть 10 минут (как и ты — тоже отдохни!).
    5. Жарь на горячей сковороде ~1 мин с каждой стороны.
    6. Подавай со сметаной, вареньем или мёдом. 🍯

  ─────────────────────────────────────
  💡 Совет от бабушки: если блин рвётся —
     тесто слишком густое. Как и код.
     Разбавь молоком. Или рефактори.
`;

// ── Напоминания ───────────────────────────────────────────────────────────────

const REMINDERS = [
  "Ты последний раз ел... когда? Сделай перерыв! 🍽️",
  "Разработчик без еды — как агент без токена. Иди поешь! 🪆",
  "Git commit сделан? Теперь — на кухню! Блины сами себя не перевернут.",
  "Дедлайн дедлайном, а обед по расписанию. Бабушка сказала.",
  "Ты уже 4 часа в коде. Твой мозг требует блинов. Это не баг — это фича.",
  "Помни: stack overflow не поможет, если ты голодный. Поешь сначала.",
  "Хорошие блины пекутся терпеливо. Хороший код — тоже. Заодно поешь.",
  "Матрёшка-агент работает без еды. Ты — нет. Иди на кухню!",
];

// ── Команда ───────────────────────────────────────────────────────────────────

export function blinyCommand(): void {
  const reminder = REMINDERS[Math.floor(Math.random() * REMINDERS.length)] ?? REMINDERS[0]!;

  // Начисляем XP за заботу о себе
  const newLevel = addMatryoshkaXp(5);
  const xpLine = `+5 XP за перерыв! Текущий уровень: ${newLevel.emoji} ${newLevel.name}`;

  process.stdout.write(`${BLINY_ART}\n`);
  process.stdout.write(`  💬 ${reminder}\n`);
  process.stdout.write(`${RECIPE}\n`);
  process.stdout.write(`  ${xpLine}\n\n`);
}
